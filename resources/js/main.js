function renClntSec() {
    var $slider = $('.client-logo-slider'),
        counter = 0,
        $logoDiv,
        $img;

    for (; counter < clientLogo.length; counter++) {
        $logoDiv = $('<div>').attr({
            class: "clnt-logo-img-holder"
        }).css({
            'height': '150px',
            'margin': '0 10px'
        });
        $img = $('<img>').attr({
            src: './resources/client_logos/' + clientLogo[counter],
            class: 'clnt-logos-img'
        }).css({
            'height': '150px',
            'max-width': '200px'
        });

        $logoDiv.append($img);
        $slider.append($logoDiv);
    }

    $('.client-logo-slider').slick({
        slidesToShow: mobilecheck() ? 2 : 4,
        slidesToScroll: 1,
        arrows: false,
        autoplay: true,
        autoplaySpeed: 2000
    });

}

function filterData(fileData) {
    var finArr = [],
        resultArr = [],
        allobj = {
            id: "all",
            label: "All",
            data: []
        };
    finArr.push(allobj);
    $.each(fileData, (key, value) => {
        resultObj = {};
        resultArr = [];

        $.each(value.data, (keyval, obj) => {
            imgData = [];
            $.each(obj.imgData, (index, val) => {
                imgData.push(value.basePath + val);
            });
            resultArr.push({
                imgData: imgData,
                isSlider: imgData.length > 1,
                pdfLink: obj.pdfName ? value.basePath + obj.pdfName : '',
                videoFrame: obj.videoFrame || ''
            });
            if (key !== 'films') {
                allobj.data.push({
                    imgData: imgData,
                    isSlider: imgData.length > 1,
                    pdfLink: obj.pdfName ? value.basePath + obj.pdfName : '',
                    videoFrame: obj.videoFrame || ''
                });
            }
        });

        finArr.push({
            id: key,
            label: value.label,
            data: resultArr
        });
    });
    allobj.data = shuffle(allobj.data);

    return finArr;
}

function shuffle(array) {
    return array.sort(() => Math.random() - 0.5);
}

